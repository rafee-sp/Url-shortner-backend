package com.url.shortner.service;

import java.awt.image.BufferedImage;
import java.io.ByteArrayOutputStream;
import java.util.Base64;

import javax.imageio.ImageIO;

import org.springframework.stereotype.Service;

import com.google.zxing.BarcodeFormat;
import com.google.zxing.client.j2se.MatrixToImageWriter;
import com.google.zxing.common.BitMatrix;
import com.google.zxing.qrcode.QRCodeWriter;

@Service
public class QRcodeService {

	public String generateQr(String shortUrl) throws Exception {
		
		QRCodeWriter qrCodeWriter = new QRCodeWriter();
		
		BitMatrix bitMatrix = qrCodeWriter.encode(shortUrl, BarcodeFormat.QR_CODE, 300, 300);
		
		BufferedImage qrCode = MatrixToImageWriter.toBufferedImage(bitMatrix);
		
		ByteArrayOutputStream outStream = new ByteArrayOutputStream();
		ImageIO.write(qrCode, "png", outStream);
		
		String base64Value = Base64.getEncoder().encodeToString(outStream.toByteArray());
		
		String encodedQr = "data:image/png;base64,"+base64Value;
		
		return encodedQr;
		  
	}
}
